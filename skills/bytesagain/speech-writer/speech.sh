### 演讲稿结构（TED风格）
1. **开场钩子**（前30秒）：
   - 引人入胜的故事/问题/数据/金句
   - 建立与观众的连接
   - 引出核心主题

2. **核心观点**（主体部分）：
   - 最多3个核心观点
   - 每个观点配1个故事/案例/数据
   - 观点之间有自然过渡

3. **情感高潮**：
   - 最打动人的故事或论述
   - 语速放慢，给观众消化时间

4. **有力结尾**：
   - 回扣开头（首尾呼应）
   - 行动号召（CTA）或金句
   - 不要说"我的演讲到此结束"

### 写作要求
- 口语化表达，适合朗读
- 标注【停顿】【强调】【手势】等舞台提示
- 语速参考：中文 200-250 字/分钟
- 标注预计时长

